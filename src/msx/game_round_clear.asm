SECTION code_user
; ====================================================================================================
; ラウンドクリアー
; ====================================================================================================
ROUND_CLEAR:
    ; ■初回のみの処理実行
    JR Z,ROUND_CLEAR_INIT


ROUND_CLEAR_EXIT:
    RET

; ----------------------------------------------------------------------------------------------------
; ラウンドクリアー時の初回処理
; ----------------------------------------------------------------------------------------------------
ROUND_CLEAR_INIT:
    NOP

ROUND_CLEAR_INIT_EXIT:
    RET

SECTION rodata_user
; ====================================================================================================
; 定数エリア
; romに格納される
; ====================================================================================================

STRING_ROUND_CLEAR:
    DW $010A
	DB "ROUND CLEAR!",0
